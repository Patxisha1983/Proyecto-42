#!/usr/bin/env python3

# Iniciar el bucle while
while True:
	# Pedir al usuario que ingrese algo
	user_input = input("What you gotta say? : ")

	# Si el usuario ingresa "STOP", detener el bucle
	if user_input == "STOP" :
		break

	# Responder con "I got that! Anything else?"
	print("I got that! Anything else?")

