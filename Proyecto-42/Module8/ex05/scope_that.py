#!/usr/bin/python3

# Definir el método add_one
def add_one(x):
    return x + 1

# Inicializar una variable
my_variable = 5

# Mostrar el valor de la variable antes de llamar al método
print("Before calling add_one:", my_variable)

# Llamar al método add_one
my_variable = add_one(my_variable)

# Mostrar el valor de la variable después de llamar al método
print("After calling add_one:", my_variable)
